/*In JavaScript, conditional statements are used to control the flow of a program based on certain conditions. The common conditional statements in JavaScript include `if`, `else if`, and `else`. Here's an explanation with syntax and examples:

1. **if statement:**
   The `if` statement allows you to execute a block of code if a specified condition is true.

   Syntax:
   ```javascript
   if (condition) {
       // Code to be executed if the condition is true
   }
   ```

   Example:
   ```javascript
   let x = 10;
   if (x > 5) {
       console.log("x is greater than 5");
   }
   ```

2. **if-else statement:**
   The `if-else` statement allows you to execute one block of code if the specified condition is true and another block if it is false.

   Syntax:
   ```javascript
   if (condition) {
       // Code to be executed if the condition is true
   } else {
       // Code to be executed if the condition is false
   }
   ```

   Example:
   ```javascript
   let x = 3;
   if (x > 5) {
       console.log("x is greater than 5");
   } else {
       console.log("x is not greater than 5");
   }
   ```

3. **if-else if-else statement:**
   The `if-else if-else` statement allows you to check multiple conditions and execute different blocks of code accordingly.

   Syntax:
   ```javascript
   if (condition1) {
       // Code to be executed if condition1 is true
   } else if (condition2) {
       // Code to be executed if condition2 is true
   } else {
       // Code to be executed if none of the conditions are true
   }
   ```

   Example:
   ```javascript
   let x = 5;
   if (x > 5) {
       console.log("x is greater than 5");
   } else if (x < 5) {
       console.log("x is less than 5");
   } else {
       console.log("x is equal to 5");
   }
   ```

These examples demonstrate the use of conditional statements in JavaScript to make decisions 
based on certain conditions. The code inside the blocks is executed only if the specified conditions are met.
*/